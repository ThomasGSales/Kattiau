/*!50003 CREATE DEFINER=`root`@`localhost` PROCEDURE `popula_detalhes_pedido`(v1 INT)
BEGIN
  DECLARE i INT DEFAULT 0;
  DECLARE id_pedido INT;
  DECLARE id_produto INT;
  WHILE i < v1 DO
    -- Certificar-se de que os IDs são válidos e não nulos
    SET id_pedido = (SELECT FLOOR(RAND() * (SELECT COALESCE(MAX(ID_Pedido), 1) FROM Pedido) + 1));
    SET id_produto = (SELECT FLOOR(RAND() * (SELECT COALESCE(MAX(ID_Produto), 1) FROM Produto) + 1));
    -- Verifica se a combinação (ID_Pedido, ID_Produto) já existe
    IF NOT EXISTS (SELECT 1 FROM DetalhesPedido WHERE ID_Pedido = id_pedido AND ID_Produto = id_produto) THEN
      INSERT INTO DetalhesPedido (ID_Pedido, ID_Produto, Quantidade, PrecoUnitario)
      VALUES (
        id_pedido,
        id_produto,
        FLOOR(RAND() * 10 + 1),
        (SELECT Preco FROM Produto WHERE ID_Produto = id_produto)
      );
      SET i = i + 1;
    END IF;
  END WHILE;
END */$$
